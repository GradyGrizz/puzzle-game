const FONT_W = 5, FONT_H = 7, FONT_SP = 1;

// width in pixels of a string at scale s
function textWidth(str, s) {
  return str.length * (FONT_W + FONT_SP) * s - FONT_SP * s;
}

// align: 'left' | 'center' | 'right'
function drawText(ctx, str, x, y, s, color, align, shadow) {
  str = String(str).toUpperCase();
  let w = textWidth(str, s);
  if (align === 'center') x -= Math.round(w / 2);
  else if (align === 'right') x -= w;
  x = Math.round(x); y = Math.round(y);
  if (shadow) {
    drawTextRaw(ctx, str, x + s, y + s, s, shadow);
  }
  drawTextRaw(ctx, str, x, y, s, color);
  return w;
}

function drawTextRaw(ctx, str, x, y, s, color) {
  ctx.fillStyle = color;
  let cx = x;
  for (let i = 0; i < str.length; i++) {
    const g = FONT_GLYPHS[str[i]] || FONT_GLYPHS['?'];
    for (let r = 0; r < FONT_H; r++) {
      const row = g[r];
      if (!row) continue;
      for (let c = 0; c < FONT_W; c++) {
        if (row & (0x10 >> c)) ctx.fillRect(cx + c * s, y + r * s, s, s);
      }
    }
    cx += (FONT_W + FONT_SP) * s;
  }
}

// Draw text no wider than maxW: shrink the scale first, then
// truncate with '...' if scale 1 still overflows. Returns width.
function drawTextFit(ctx, str, x, y, maxW, s, color, align, shadow) {
  str = String(str);
  while (s > 1 && textWidth(str, s) > maxW) s--;
  if (textWidth(str, s) > maxW) {
    while (str.length > 1 && textWidth(str + '...', s) > maxW) str = str.slice(0, -1);
    str += '...';
  }
  return drawText(ctx, str, x, y, s, color, align, shadow);
}

// Word-wrap text into lines that fit maxWidth at scale s
function wrapText(str, s, maxWidth) {
  const words = String(str).split(' ');
  const lines = [];
  let cur = '';
  for (const w of words) {
    const test = cur ? cur + ' ' + w : w;
    if (textWidth(test, s) > maxWidth && cur) { lines.push(cur); cur = w; }
    else cur = test;
  }
  if (cur) lines.push(cur);
  return lines;
}
